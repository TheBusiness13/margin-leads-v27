create index if not exists activity_logs_workspace_event_idx on public.activity_logs(workspace_id,event_type,created_at desc);

create or replace function public.is_workspace_member(wid uuid)
returns boolean language sql stable security definer set search_path=public as $$
  select exists(select 1 from public.workspace_members where workspace_id=wid and user_id=(select auth.uid()))
      or exists(select 1 from public.workspaces where id=wid and owner_id=(select auth.uid()))
$$;
create or replace function public.workspace_role(wid uuid)
returns text language sql stable security definer set search_path=public as $$
  select coalesce(
    (select role from public.workspace_members where workspace_id=wid and user_id=(select auth.uid()) limit 1),
    (select 'owner' from public.workspaces where id=wid and owner_id=(select auth.uid()) limit 1)
  )
$$;

-- Policies are deliberately replaced so this file remains repeat-safe.
drop policy if exists "workspace members can view workspaces" on public.workspaces;
drop policy if exists "owners can create workspaces" on public.workspaces;
drop policy if exists "members can view memberships" on public.workspace_members;
drop policy if exists "owners and admins manage memberships" on public.workspace_members;
drop policy if exists "members manage campaigns" on public.campaigns;
drop policy if exists "members read campaigns" on public.campaigns;
drop policy if exists "operators manage campaigns" on public.campaigns;
drop policy if exists "members manage leads" on public.leads;
drop policy if exists "members read leads" on public.leads;
drop policy if exists "operators manage leads" on public.leads;
drop policy if exists "members view logs" on public.activity_logs;
drop policy if exists "members create logs" on public.activity_logs;

create policy "workspace members can view workspaces" on public.workspaces for select to authenticated using(public.is_workspace_member(id));
create policy "owners can create workspaces" on public.workspaces for insert to authenticated with check(owner_id=(select auth.uid()));
create policy "members can view memberships" on public.workspace_members for select to authenticated using(public.is_workspace_member(workspace_id));
create policy "owners and admins manage memberships" on public.workspace_members for all to authenticated
  using(public.workspace_role(workspace_id) in ('owner','admin'))
  with check(public.workspace_role(workspace_id) in ('owner','admin'));
create policy "members read campaigns" on public.campaigns for select to authenticated using(public.is_workspace_member(workspace_id));
create policy "operators manage campaigns" on public.campaigns for all to authenticated
  using(public.workspace_role(workspace_id) in ('owner','admin','operator'))
  with check(public.workspace_role(workspace_id) in ('owner','admin','operator'));
create policy "members read leads" on public.leads for select to authenticated using(public.is_workspace_member(workspace_id));
create policy "operators manage leads" on public.leads for all to authenticated
  using(public.workspace_role(workspace_id) in ('owner','admin','operator'))
  with check(public.workspace_role(workspace_id) in ('owner','admin','operator'));
create policy "members view logs" on public.activity_logs for select to authenticated using(public.is_workspace_member(workspace_id));
create policy "members create logs" on public.activity_logs for insert to authenticated
  with check(public.is_workspace_member(workspace_id) and user_id=(select auth.uid()));
