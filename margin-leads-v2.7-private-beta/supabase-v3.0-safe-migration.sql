-- MarginBusiness Leads v3.0 repeat-safe beta migration
-- Safe to run more than once. It does not delete tables or lead data.

create extension if not exists pgcrypto;

create table if not exists public.workspaces(
  id uuid primary key default gen_random_uuid(), name text not null, slug text unique,
  owner_id uuid not null references auth.users(id) on delete cascade,
  plan text not null default 'beta', created_at timestamptz not null default now()
);
create table if not exists public.workspace_members(
  workspace_id uuid references public.workspaces(id) on delete cascade,
  user_id uuid references auth.users(id) on delete cascade,
  role text not null default 'owner', created_at timestamptz not null default now(),
  primary key(workspace_id,user_id)
);
create table if not exists public.campaigns(
  id uuid primary key default gen_random_uuid(), workspace_id uuid not null references public.workspaces(id) on delete cascade,
  name text not null, source text, import_profile text, status text default 'active', settings jsonb not null default '{}'::jsonb,
  created_by uuid references auth.users(id), created_at timestamptz default now(), updated_at timestamptz default now()
);
create table if not exists public.leads(
  id uuid primary key default gen_random_uuid(), workspace_id uuid not null references public.workspaces(id) on delete cascade,
  campaign_id uuid references public.campaigns(id) on delete set null, company text, contact_name text, role text, email text,
  website text, category text, product text, status text default 'new', fit_score integer default 0,
  data jsonb not null default '{}'::jsonb, created_at timestamptz default now(), updated_at timestamptz default now()
);
create table if not exists public.activity_logs(
  id bigint generated by default as identity primary key, workspace_id uuid references public.workspaces(id) on delete cascade,
  user_id uuid references auth.users(id) on delete set null, event_type text not null, entity_type text, entity_id text,
  metadata jsonb not null default '{}'::jsonb, created_at timestamptz default now()
);

